module Main where
--------------------------------------------------------------------------
main :: IO ()
main = do
  print $ "------------------------------"
  print $ avg 1 2 3
  print $ avg 0 0 0
  print $ avg 1 1 3
  print $ "------------------------------"
--------------------------------------------------------------------------
avg :: Int -> Int -> Int -> Double
avg a b c = fromIntegral (a + b + c) / 3
