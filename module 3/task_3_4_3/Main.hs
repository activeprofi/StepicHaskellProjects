module Main where
--------------------------------------------------------------------------
main :: IO ()
main = do
  print $ "Lists"
  print $ "------------------------------"
  print $ sumOdd [2,5,30,37]
  print $ sumOdd [1,3,5,7]
  print $ sumOdd [0,2,4,5]
  print $ "------------------------------"
--------------------------------------------------------------------------
sumOdd :: [Integer] -> Integer
sumOdd = foldr f 0 where
  f x s | odd x     = x + s
        | otherwise = s
--------------------------------------------------------------------------
