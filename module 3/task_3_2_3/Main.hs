module Main where
--------------------------------------------------------------------------
main :: IO ()
main = do
  print $ "Lists"
  print $ "------------------------------"
  print $ qsort [1,3,2,5]
  print $ qsort [1,4,6,-8]
  print $ qsort [14,0,12,5]
  print $ "------------------------------"
  print $ qsort' [1,3,2,5]
  print $ qsort' [1,4,6,-8]
  print $ qsort' [14,0,12,5]
  print $ "------------------------------"
--------------------------------------------------------------------------
qsort :: Ord a => [a] -> [a]
qsort [] = []
qsort (x:xs) = qsort [y | y <- xs, y < x] ++ [x] ++ qsort [y | y <- xs, y >= x]
--------------------------------------------------------------------------
qsort' :: Ord a => [a] -> [a]
qsort' [] = []
qsort' (x:xs) = qsort' (filter (\y -> y < x) xs) ++ [x] ++ qsort' (filter (\y -> y >= x) xs)
--------------------------------------------------------------------------
