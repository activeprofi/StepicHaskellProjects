module Main where
--------------------------------------------------------------------------
main :: IO ()
main = do
  print $ "Lists"
  print $ "------------------------------"
  print $ meanList [1,2,3,4]
  print $ "------------------------------"
--------------------------------------------------------------------------
meanList :: [Double] -> Double
meanList list = someFun (foldr (\x (s,l) -> (s + x,l + 1)) (0,0) list)
--------------------------------------------------------------------------
someFun p = uncurry (/) p
--------------------------------------------------------------------------
