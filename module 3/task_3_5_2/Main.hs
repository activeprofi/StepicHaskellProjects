module Main where
--------------------------------------------------------------------------
main :: IO ()
main = do
  print $ "Lists"
  print $ "------------------------------"
  print $ evenOnly [1..10]
  print $ evenOnly ['a'..'z']
  print $ "------------------------------"
--------------------------------------------------------------------------
evenOnly :: [a] -> [a]
evenOnly = foldr (\(n,x) xs -> if even n then x:xs else xs) [] . zip [1..]
--------------------------------------------------------------------------
