module Main where
--------------------------------------------------------------------------
main :: IO ()
main = do
  print $ "Lists"
  print $ "------------------------------"
  print $ perms [1,2,3]      
  print $ "------------------------------"
--------------------------------------------------------------------------
perms :: [a] -> [[a]]
perms []     = [[]]
perms (x:xs) = concatMap (insert x) (perms xs)
--------------------------------------------------------------------------
insert :: a -> [a] -> [[a]]
insert x []     = [[x]]
insert x (y:ys) = (x:y:ys) : map (y:) (insert x ys)
--------------------------------------------------------------------------
