module Main where
--------------------------------------------------------------------------
main :: IO ()
main = do
  print $ "Lists"
  print $ "------------------------------"
  print $ lastElem [1..10]
  print $ lastElem ['a'..'z']
  print $ lastElem [1]
  print $ lastElem [1,6,-5,50]
  print $ "------------------------------"
--------------------------------------------------------------------------
lastElem :: [a] -> a
lastElem = foldl1 (\_ xs -> xs)
