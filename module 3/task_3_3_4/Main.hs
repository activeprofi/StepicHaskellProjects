module Main where
--------------------------------------------------------------------------
main :: IO ()
main = do
  print $ "Lists"
  print $ "------------------------------"
  print $ change 7
  print $ "------------------------------"
--------------------------------------------------------------------------
coins :: (Ord a, Num a) => [a]
coins = [2,3,7]
--------------------------------------------------------------------------
change :: (Ord a, Num a) => a -> [[a]]
change 0 = [[]]
change c = [coin:ch | coin <- coins, coin <= c, ch <- change $ c - coin]
--------------------------------------------------------------------------
