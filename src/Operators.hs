module Operators (operatorsMain)
where
--------------------------------------------------------------------------
operatorsMain :: IO()
operatorsMain = do
  print (3 *+* 4)
  print ((*+*) 3 4)
  print (1 + 3 *+* 2 * 2)
  print (5 |-| 7)
  print ((2 /) 2)
  print ((/ 2) 4)
--------------------------------------------------------------------------
infixl 6 *+*
a *+* b = a ^ 2 + b ^ 2
--------------------------------------------------------------------------
infixl 6 |-|
a |-| b = abs (a - b)
